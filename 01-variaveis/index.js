/*Utilizamos váriaveis para alocar valores na memória do computador, 
dentro da linguagem javascript temos as palavras chaves
var, o let e o const*/
/*
var - Pode ser usada sem necessariamente ser inicializada com
algum tipo de valor (string ou number), não é indicado o uso
devido a alguns problemas encontrados em códigos mais complexos

let - Foi pensado para trazer o escopo de bloco, ou seja a variavel
declarada em um bloco, somente será utilizada dentro do bloco.
Os valores de uma variavel let tambem podem ser alterados ao longo 
de sua utilização.

const - Aqui é onde declara-se uma constante, o valor inicial inputado
não pode ser alterado em nenhum momento.
*/
const nome = "Diego";

console.log(nome);